    public boolean isEmpty() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn hãy nhập tên username!!!", "Canh bao", 2);
            return false;
        }
        if (String.valueOf(jPasswordField1.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn hãy nhập password!!!", "Canh bao", 2);
            return false;
        }
        return true;
    }

